#pragma once

#include <functional>

namespace stdext {

//
// Implementation for hash_combine missing from boost/STL/C++11.
// Not needed once C++14 is out, hopefully.
//
template <class T>
inline void hash_combine(std::size_t& seed, const T& v)
{
    std::hash<T> hasher;
    seed ^= hasher(v) + 0x9e3779b9 + (seed<<6) + (seed>>2);
}

} // stdext namespace
